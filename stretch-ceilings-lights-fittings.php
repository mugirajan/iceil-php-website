                                <h5 class="sin-blog" style="font-size: 25px; padding-bottom: 10px;">How do stretch ceilings work with lights and other fittings and fixtures?
</h5>
                                <p>In residential or commercial area stretch ceiling are mostly used nowadays. Some people don’t know how stretch ceiling will make our place more functionable. They might even think how they will fix light or fire alarm sensor in the stretch ceiling. The concept behind the stretch ceiling is very simple. Stretch ceiling is formed with different layer. According to the choice of the stretch ceiling, metal or wood support given to the surface of the existing ceiling. Aluminium track is then placed to attach the ceiling membrane. There will be some space between the stretch ceiling and existing ceiling where we can fix equipment’s for lighting, burglar alarm etc. In this article, let us discuss how lights are fitted to the stretch ceiling.</p>
                                <p>Lighting in the stretch ceiling system</p>
                                <p>Normally, while installing the stretch ceiling some kind of metal, foam or acrylic rings are fixed on the existing surface. Using this rings support, lighting facility are given to the ceiling surface. Focal lights like chandelier, halogen lights can be inserted to stretch ceiling by slightly piercing the canvas material of stretch ceiling.</p>
                                <p>In some kind of stretch ceiling, lights are provided as default option. For example, if we choose translucent ceiling, led lights are fixed all over the place. This will give back lit effect to your interior. While fitting external lights to the stretch ceiling, the ceiling membrane might get damaged. To avoid that strengthening rings are used. It is not visible to our eyes. Hope you got an idea about stretch ceiling lighting.</p>
