                                <p>Stretch ceiling is more popular nowadays as it’s a customized lighting solution as well. Illuminating with stretch ceiling makes the space more appealing.  When considering illuminated stretch ceiling, one of the main concern is the power consumption and its cost of operation. In this article let us discuss on stretch ceiling power consumption and the cost associated to it.</p>
                                <img src="https://i0.wp.com/iceilsystems.com/wp-content/uploads/2022/05/Stretch-ceiling_power-consumption.jpg?resize=768%2C587&ssl=1" alt=""> <br><br>
                                <h6>Energy consumption of stretch ceiling</h6>
                                <p>LED lighting solution is generally used in stretch ceilings as it uniformly transmits light across the stretch ceiling. We at Iceil use high energy efficient LED Rigid Bars for designing translucent ceiling. The number of LED Rigid bars and its associated power consumption will depend on the distance between the light source and the stretch ceiling film. On an average 100 SQFT area, the energy consumption will be around 500 – 600 watts. A dimmer can be attached to the systems to reduce the brightness, in which case the actual power consumption will be directly proportional to the brightness %. <br><br>
                                When it comes to electricity unit consumption for the 500 – 600 Watts, it will be 15 Units per 24 Hours.  In State of Tamil Nādu in India unit cost of power for Residential sector is INR 5 and Commercial sector is INR 8. If we use the light for 24 hours, the price of power usage will be INR 60 for Residential and INR 120 for commercial.  Generally it is recommended to consume power by letting them cool down for 1-2 hour in-between every 10-12 hours usage.</p>
                                
